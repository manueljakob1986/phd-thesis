function PT_storage_capacity = c23_input_PT_storage_capacity
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% This input parameter describes the average PT storage capacity based on
% the number of passengers that maximally fit into one PT vehicle.
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
 
PT_storage_capacity = (117 + 65)/2; %on average 91 passengers

% Trams on average = 117 passengers (source:
% https://www.stadt-zuerich.ch/vbz/de/index/die_vbz/fahrzeuge/trams/tram2000.html)

% Bus on average = 65 passengers (source:
% https://www.stadt-zuerich.ch/vbz/de/index/die_vbz/fahrzeuge/autobusse/neoplan_centroliner_standard.html)
 
end